#include <iostream>
using namespace std;
int main()
{
    int a = 9 ;
    cout<<"SIZE OF INT" << sizeof(a)<<endl;

    float b = 9.9 ;
    cout<<"SIZE OF FLOAT" << sizeof(b)<<endl;

    bool c = true ;
    cout<<"SIZE OF BOOL" << sizeof(c)<<endl;

    char d = 'a' ;
    cout<<"SIZE OF CHAR" << sizeof(d)<<endl;

    short int e = 9 ;
    cout<<"SIZE OF SHORT INT" << sizeof(e)<<endl;

    long int f = 9 ;
    cout<<"SIZE OF LONG INT" << sizeof(f)<<endl;
    return 0;
    
}